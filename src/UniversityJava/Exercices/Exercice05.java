package UniversityJava.Exercices;

public class Exercice05 {
    public static void main(String[] args) {
        // Ciclo Do While
        // Entra al menos una vez

        var contador = 0;

        do {
            System.out.println("Recorrido: " + contador);
            contador++;
        } while (contador <= 3);
    }
}
